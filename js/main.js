        // Quiz state
        let currentQuestionIndex = 0;
        let userAnswers = [];
        let selectedCategories = ["all"];
        let filteredQuestions = [];

        // DOM elements
        const startScreen = document.getElementById('start-screen');
        const quizScreen = document.getElementById('quiz-screen');
        const resultsScreen = document.getElementById('results-screen');
        const startBtn = document.getElementById('start-btn');
        const prevBtn = document.getElementById('prev-btn');
        const nextBtn = document.getElementById('next-btn');
        const finishBtn = document.getElementById('finish-btn');
        const restartBtn = document.getElementById('restart-btn');
        const reviewBtn = document.getElementById('review-btn');
        const questionText = document.getElementById('question-text');
        const optionsContainer = document.getElementById('options-container');
        const currentQuestionEl = document.getElementById('current-question');
        const totalQuestionsEl = document.getElementById('total-questions');
        const progressBar = document.getElementById('progress');
        const finalScoreEl = document.getElementById('final-score');
        const correctCountEl = document.getElementById('correct-count');
        const incorrectCountEl = document.getElementById('incorrect-count');
        const categoryBtns = document.querySelectorAll('.category-btn');

        // Event listeners
        startBtn.addEventListener('click', startQuiz);
        prevBtn.addEventListener('click', showPreviousQuestion);
        nextBtn.addEventListener('click', showNextQuestion);
        finishBtn.addEventListener('click', showResults);
        restartBtn.addEventListener('click', restartQuiz);
        reviewBtn.addEventListener('click', reviewAnswers);

        // Category selection
        categoryBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                const category = btn.getAttribute('data-category');
                
                if (category === 'all') {
                    // Toggle all categories
                    if (selectedCategories.includes('all')) {
                        selectedCategories = [];
                        btn.classList.remove('active');
                    } else {
                        selectedCategories = ['all'];
                        categoryBtns.forEach(b => b.classList.remove('active'));
                        btn.classList.add('active');
                    }
                } else {
                    // Toggle specific category
                    if (selectedCategories.includes('all')) {
                        selectedCategories = [category];
                        categoryBtns.forEach(b => b.classList.remove('active'));
                        btn.classList.add('active');
                    } else {
                        if (selectedCategories.includes(category)) {
                            selectedCategories = selectedCategories.filter(c => c !== category);
                            btn.classList.remove('active');
                            
                            // If no categories selected, select 'all'
                            if (selectedCategories.length === 0) {
                                selectedCategories = ['all'];
                                document.querySelector('.category-btn[data-category="all"]').classList.add('active');
                            }
                        } else {
                            selectedCategories.push(category);
                            btn.classList.add('active');
                        }
                    }
                }
            });
        });

        // Initialize the quiz
        function initQuiz() {
            // Filter questions based on selected categories
            if (selectedCategories.includes('all')) {
                filteredQuestions = [...quizData];
            } else {
                filteredQuestions = quizData.filter(question => 
                    selectedCategories.includes(question.category)
                );
            }
            
            // Shuffle questions for variety
            filteredQuestions = shuffleArray(filteredQuestions);
            
            // Reset quiz state
            currentQuestionIndex = 0;
            userAnswers = new Array(filteredQuestions.length).fill(null);
            
            // Update UI
            totalQuestionsEl.textContent = filteredQuestions.length;
            updateProgressBar();
            showQuestion();
        }

        // Start the quiz
        function startQuiz() {
            startScreen.classList.add('hidden');
            quizScreen.classList.remove('hidden');
            initQuiz();
        }

        // Show current question
        function showQuestion() {
            if (currentQuestionIndex >= filteredQuestions.length) {
                showResults();
                return;
            }
            
            const question = filteredQuestions[currentQuestionIndex];
            questionText.textContent = question.question;
            currentQuestionEl.textContent = currentQuestionIndex + 1;
            
            // Clear previous options
            optionsContainer.innerHTML = '';
            
            // Create new options
            question.options.forEach((option, index) => {
                const optionElement = document.createElement('div');
                optionElement.className = 'option';
                optionElement.textContent = option;
                optionElement.dataset.index = index;
                
                // Check if this option was previously selected
                if (userAnswers[currentQuestionIndex] === index) {
                    optionElement.classList.add('selected');
                    
                    // If answer was already submitted, show feedback
                    if (userAnswers[currentQuestionIndex] !== null) {
                        showFeedback(question, index);
                    }
                }
                
                optionElement.addEventListener('click', selectOption);
                optionsContainer.appendChild(optionElement);
            });
            
            // Update navigation buttons
            prevBtn.disabled = currentQuestionIndex === 0;
            nextBtn.disabled = currentQuestionIndex === filteredQuestions.length - 1;
            
            // Show/hide finish button
            if (currentQuestionIndex === filteredQuestions.length - 1) {
                finishBtn.classList.remove('hidden');
                nextBtn.classList.add('hidden');
            } else {
                finishBtn.classList.add('hidden');
                nextBtn.classList.remove('hidden');
            }
            
            // Update progress bar
            updateProgressBar();
        }

        // Select an option
        function selectOption(e) {
            const selectedOption = e.currentTarget;
            const optionIndex = parseInt(selectedOption.dataset.index);
            const question = filteredQuestions[currentQuestionIndex];
            
            // Remove selected class from all options
            document.querySelectorAll('.option').forEach(option => {
                option.classList.remove('selected');
            });
            
            // Add selected class to clicked option
            selectedOption.classList.add('selected');
            
            // Store user's answer
            userAnswers[currentQuestionIndex] = optionIndex;
            
            // Show immediate feedback
            showFeedback(question, optionIndex);
        }

        // Show immediate feedback
        function showFeedback(question, selectedIndex) {
            const isCorrect = selectedIndex === question.correctAnswer;
            
            // Highlight correct and incorrect answers
            document.querySelectorAll('.option').forEach((option, index) => {
                if (index === question.correctAnswer) {
                    option.classList.add('correct');
                } else if (index === selectedIndex && !isCorrect) {
                    option.classList.add('incorrect');
                }
            });
            
            // Disable further selection
            document.querySelectorAll('.option').forEach(option => {
                option.style.pointerEvents = 'none';
            });
        }

        // Show previous question
        function showPreviousQuestion() {
            if (currentQuestionIndex > 0) {
                currentQuestionIndex--;
                showQuestion();
            }
        }

        // Show next question
        function showNextQuestion() {
            if (currentQuestionIndex < filteredQuestions.length - 1) {
                currentQuestionIndex++;
                showQuestion();
            }
        }

        // Show quiz results
        function showResults() {
            quizScreen.classList.add('hidden');
            resultsScreen.classList.remove('hidden');
            
            // Calculate score
            let correctCount = 0;
            filteredQuestions.forEach((question, index) => {
                if (userAnswers[index] === question.correctAnswer) {
                    correctCount++;
                }
            });
            
            const score = Math.round((correctCount / filteredQuestions.length) * 100);
            
            // Update results UI
            finalScoreEl.textContent = `${score}%`;
            correctCountEl.textContent = correctCount;
            incorrectCountEl.textContent = filteredQuestions.length - correctCount;
        }

        // Restart quiz
        function restartQuiz() {
            resultsScreen.classList.add('hidden');
            startScreen.classList.remove('hidden');
        }

